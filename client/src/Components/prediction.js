function estimate_coef(x,y){
    var n = _.size(x);

    var m_x = _.mean(x); 
    var m_y = _.mean(y);

    var sum_xy = 0;
    var sum_xx = 0;

    for(var i=0;i<n;i++){
        sum_xy = x[i]*y[i]+sum_xy;
        sum_xx = x[i]*x[i]+sum_xx;
    }

    var SS_xy = sum_xy - n*m_x*m_y;
    var SS_xx = sum_xx - n*m_x*m_x;

    var b_1 = SS_xy / SS_xx ;
    var b_0 = m_y - b_1*m_x;

    var b = [];
    b[0] = b_0;
    b[1] = b_1;
    return(b);
}
